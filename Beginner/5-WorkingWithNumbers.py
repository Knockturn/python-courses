from math import * # Importing the math module

print(2 + 1.34)
print(3 * (4 + 5))
print(10 % 3) # Modulo operator (remainder)
my_num = 16
print(str(my_num) + " is my favorite number") # str() is a function that converts a number to a string
print(f"{my_num} is my favorite number") # f-string
print(abs(-5)) # abs() is a function that returns the absolute value of a number
print(pow(3, 3)) # pow() is a function that returns the first number to the power of the second number
print(max(4, 6)) # max() is a function that returns the largest number
print(min(4, 6)) # min() is a function that returns the smallest number
print(round(3.5)) # round() is a function that rounds a number

# Use match function below
print(floor(3.7)) # floor() is a function that rounds a number down
print(ceil(3.7)) # ceil() is a function that rounds a number up
