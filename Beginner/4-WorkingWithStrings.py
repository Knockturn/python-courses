print("Giraffe \n Academy") # \n is a new line
print("Giraffe \" Academy") # \" is a double quote. \ is an escape character
phrase = "Giraffe Academy"
print(phrase + " is cool")
print(phrase.lower()) # lower() is a method
print(phrase.upper()) # upper() is a method
print(phrase.upper().isupper()) # isupper() is a method that returns a boolean
print(len(phrase)) # len() is a function that returns the length of a string
print(phrase[0]) # returns the first character of the string
print(phrase.index("G")) # returns the index of the first occurrence of the character
print(phrase.replace("Giraffe", "Elephant")) # replace() is a method that replaces a string with another string